
using System;
namespace TestDotNetVrSystem
{
    public class Task2
    {
        /*
         * Construa um método que receba uma string contendo um valor de placa de carro e 
         * retorne um bool de acordo com a validação:
         * 
         * Obs.: Estamos com o padrão brasileiro "antigo" e as placas não terão '-' (traço) 
         * o qual se apresenta na forma de 3 letras maiúsculas (A-Z) seguidas  de 4 dígitos (0-9)
         * 
         * Exemplos de entradas validas: 
         * 1 - ABC1234
         * 2 - QAZ0987
         * 3 - WSX1324
         * 
         * Exemplos de entradas inválidas: 
         * 1 - ABC-1234
         * 2 - QAZA987
         * 3 - abc1324
         */
        public static bool CheckInput(string input)
        {
            int i;

            if(input.Length != 7) return false;

            for( i =0; i< input.Length; i++ ) {

                if (i <= 2 && (Char.IsDigit(input[i])  || (Char.IsLower(input[i])))) {
                    return false;
                }

                if (i > 2 && Char.IsLetter(input[i])) {
                    return false;
                }
            }

            return true;
        
        }
    }
}
